class Solution {

    private Map<Character, String> mappingRecord;
    private List<String> res;

        {
            mappingRecord = new HashMap<Character, String>() {{
                put('2',"abc");
                put('3',"def");
                put('4',"ghi");
                put('5',"jkl");
                put('6',"mno");
                put('7',"pqrs");
                put('8',"tuv");
                put('9',"wxyz");
            }};

            res = new ArrayList<>();
        }

        public List<String> letterCombinations(String digits) {
            if (digits == null || digits.length() == 0) {
                return res;
            }

            search("", digits, 0);
            return res;
        }

        private void search(String s, String digits, int i) {

            // terminator -> process -> drill down (è¿›å…¥ä¸‹ä¸€å±‚) -> reverse

            if (i == digits.length()) {
                res.add(s);
                return;
            }

            // æ‹¿åˆ°ç¬¬iä¸ªå¯¹åº”çš„å­—ç¬¦
            String letters = mappingRecord.get(digits.charAt(i));
            for (int j = 0; j < letters.length(); j++) {
                search(s + letters.charAt(j), digits, i + 1);
            }
            
        }
    }
